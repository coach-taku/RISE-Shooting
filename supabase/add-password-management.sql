--
-- 【目的】
--   共通の「合言葉（rise2024）」から、各ユーザー固有のパスワードへ移行する。
--   コーチが管理画面から選手のパスワードを確認・変更できるようにする。
--
-- 【変更内容】
--   1. profiles テーブルに password_plain カラムを追加
--      (コーチが管理画面でパスワードを確認するための表示用カラム)
--   2. profiles テーブルの username 更新ポリシーをコーチにも許可
--   3. コーチがパスワードを変更したとき、password_plain も自動更新するロジックは
--      アプリ側 (update-player API) で担当するため、DB トリガーは不要。
-- =====================================================

-- =====================================================
-- 1. profiles テーブルに password_plain カラムを追加
-- =====================================================
-- このカラムは「コーチが管理画面で選手のパスワードを確認する」ための表示専用。
-- 実際の認証は Supabase Auth (auth.users) が行う。
-- 既にカラムがある場合はスキップされる。

alter table public.profiles
  add column if not exists password_plain text;

-- コメント追記（任意）
comment on column public.profiles.password_plain is
  'コーチが管理画面で確認できるパスワード（平文）。認証はSupabase Authが担当。';

-- =====================================================
-- 2. RLS ポリシー: コーチによる profiles 更新を許可
-- =====================================================
-- 既存ポリシー「profiles_update_own」は自分自身のみ更新可能だが、
-- コーチがAPIサーバー経由（Service Role Key）で更新するため RLS をバイパスする。
-- → update-player API は createAdminClient() (Service Role) を使用しているため
--   RLS ポリシーの変更は不要。Service Role は RLS を自動的にバイパスする。

-- =====================================================
-- 3. 既存ユーザーの password_plain 初期値設定
-- =====================================================
-- 既存のデモユーザー（rise2024 を使用していたユーザー）に初期値を設定する。
-- ※ この UPDATE を実行すると、管理画面に「rise2024」と表示されるようになります。
-- ※ 実際のパスワードが変更されている場合は手動で更新してください。

-- デモユーザー全員の初期パスワードを rise2024 として記録
-- （実際に Supabase Auth 側でそのパスワードが設定されている前提）
update public.profiles
set password_plain = 'rise2024'
where password_plain is null;

-- =====================================================
-- 4. パスワード最小文字数を4文字に緩和するための Supabase ダッシュボード設定
-- =====================================================
-- ⚠️ 重要: このSQLだけでは対応できません。
--    Supabase Auth 側のパスワードポリシーも変更が必要です。
--
-- 【手順】
--   1. https://supabase.com/dashboard にアクセスしてログイン
--   2. このプロジェクトを選択
--   3. 左側メニュー「Authentication」→「Providers」→「Email」を開く
--   4. 「Minimum password length」の値を「1」または「4」に変更
--   5. 「Save」ボタンで保存
--
-- 【理由】
--   Supabase Auth のデフォルトは「最小6文字」のため、
--   ダッシュボードで変更しないと4文字パスワードでのログインが
--   エラーになる場合があります。
--   ※ 選手登録・パスワード変更（Admin API経由）はこの制限をバイパスできますが、
--      ログイン時（signInWithPassword）はポリシーが適用されます。

